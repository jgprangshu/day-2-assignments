class RectangleResult{

		public static void main(String[]args)
		{
			Rectangle object1=new Rectangle(10,20);
			System.out.println("The area of the 1st Rectangle is = " +object1.area());

			Rectangle object2=new Rectangle(40,20);
			System.out.println("The area of the 2nd Rectangle is = " +object2.area());
			
			System.out.println("The perimeter of the 1st Rectangle is = " +object1.perimeter());
			System.out.println("The perimeter of the 1st Rectangle is = " +object2.perimeter());
				
			
		}




}